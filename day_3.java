public class day_3 {
    public static void main(String[] args) {
        // for loop
        for(int i = 0; i< 3; i = i+1){
            System.out.println("hello world");
        }
        System.out.println();
        for(int a=0; a<= 3; a++){
            System.out.println(a);
        }

        System.out.println();
        // while loop
        int i=0;
        while(i< 11){
            System.out.println(i);
            i=i+1;
        }

        System.out.println();
        // do while loop
        // int i=0;
        do{
            System.out.println(i);
            i=i+1;
        } while(i<11);
    }
}
